// Statemnts o declaraciones:

// Declaración de variables
let x = 10; // Se declara la variable x y se le asigna el valor 10

// Ejemplo de declaración de condicionales
let temperatura = 25; // Se declara la variable temperatura y se le asigna el valor 25

// Estructura condicional para evaluar la temperatura
if (temperatura > 30) {
    // Si la temperatura es mayor a 30, muestra "Hace calor"
    console.log("Hace calor");
} else if (temperatura >= 20) {
    // Si la temperatura es mayor o igual a 20, muestra "Hace buen clima"
    console.log("Hace buen clima");
} else {
    // Si la temperatura es menor a 20, muestra "Hace frío"
    console.log("Hace frío");
}

