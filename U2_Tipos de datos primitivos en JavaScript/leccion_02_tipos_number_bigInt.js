//Valor numero int:

const numeroInt = 42; // Un número entero
console.log("Número entero:", numeroInt);

//Valor numero BigInt:
// En JavaScript, para declarar un BigInt se debe agregar una 'n' al final del número.
// Si no se pone la 'n', el número se interpreta como un Number normal y puede perder precisión con valores grandes.
const numeroBigInt = 123456789012345678901234567890n; // Un BigInt
console.log("Número BigInt:", numeroBigInt);